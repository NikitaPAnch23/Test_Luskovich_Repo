public class ComparisonOperators {
    public static void main(String[] args) {
        System.out.println(1434 == 4314);
        System.out.println(75 != 71);
        System.out.println(758 >= 7401);
        System.out.println(4578 <= 0);
        System.out.println(11 > 11);
        System.out.println(222 < 223);
        System.out.println(3<4 || 4>4);
        System.out.println(3>2&&5>4);
        System.out.println(3>2&&2>3);
    }
}
